    <h1>Simpson</h1>
    <p>A Simpson család (eredeti cím: The Simpsons) nagy sikerű, amerikai televíziós rajzfilmsorozat, Matt Groeningtől, amelyet a Fox tv-csatorna számára készített a Klasky Csupo stúdió (melynek alapító tagja a magyar származású Csupó Gábor). Elsőként 1987. április 19-én jelent meg mint 2 perces műsor a Tracey Ullman Show-ban. Akkora sikert aratott, hogy a Fox tévécsatorna két évvel később, 1989. december 17-én 20 perces adásban műsorára tűzte. A Simpson család azóta 32 évadot élt meg, és Matt Groeninget világhírűvé tette. Magyarországon a TV3 kezdte vetíteni 1998-tól, majd a csatorna megszűnéye óta kisebb-nagyobb, szünetekkel a Viasat 3, a Viasat 6, a magyar Comedy Central, a FOX, illetve jelenleg premierrészeket a Viasat 6. Ez a rajzfilm egy átlagosnak cseppet sem nevezhető családot mutat be, hétköznapi gondokkal. Egy középosztálybeli amerikai család életén keresztül az amerikai kultúra és társadalom világát szemlélteti humorosan. A történet egy Springfield nevű vásosban játszódik, amelyet a sorozat írója, saját bevallása szerint, az Oregon állambeli Springfieldről mintázott.</p>
